// Estrutura de repetição

// for
for (var count = 0; count <= 10; count ++) {
    console.log(count)
}

for (var i = 0; i <= 10; i ++) {
    console.log(i + 'A')
}

// while
var count = 0

while (count <= 100) {
    console.log(count)
    count++
}

// while
var i = 500

while (i > 5) {
    console.log(i)
    i /= 7
}