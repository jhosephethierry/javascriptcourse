// Operadores de comparação

// Compara valores e tipos de dados se iguais
if (10 === '10') {
    console.log('são iguais')
}
else {
    console.log('não são iguais')
}

if (10 === 10) {
    console.log('ok')
}

// Compara valores e tipos de dados se diferentes
if (10 != 9) {
    console.log('são diferentes')
}
else {
    console.log('não são diferentes')
}

// Compara valores e tipos de dados se maior ou igual
if (10 >= 9) {
    console.log('o primeiro valor é maior ou igual')
}
else {
    console.log('o primeiro valor não é maior ou igual')
}